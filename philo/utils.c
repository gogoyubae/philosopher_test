#include "philo.h"

int	ft_atoi(const char *str)
{
	int i;
	int res;

	i = 0;
	res = 0;
	while(str[i])
	{
		if (str[i] >= '0' && str[i] <= '9')
		{
			res = res * 10 + (str[i] - '0');
			i++;
		}
		else
		{
			res = -1;
			break;
		}
	}
	if (i == 0)
		res = -1;
	return (res);
}
